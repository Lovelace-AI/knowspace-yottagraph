/**
 * Generate `assets/fonts.generated.css` based on which FK Grotesk OTF files
 * actually exist in `public/fonts/`.
 *
 * Why: FK Grotesk is a commercial font; the .otf files are gitignored.
 * If we ship a hand-written fonts.css that references files that aren't
 * deployed, the browser fetches `/fonts/FKGrotesk-Regular.otf`, the SPA
 * fall-through returns the index HTML, and the browser logs
 *   "OTS parsing error: invalid sfntVersion: 1008813135"
 * (1008813135 == ASCII "<!DO"). This script only emits @font-face rules
 * for files that exist at build time, so production silently falls back
 * to the system stack instead of decoding HTML as fonts.
 *
 * Runs as `predev` and `prebuild` hooks in package.json.
 */

