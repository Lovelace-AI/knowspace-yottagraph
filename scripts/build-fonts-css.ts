function emitFace(spec: FaceSpec): string {
    return [
        '@font-face {',
        `    font-family: '${spec.family}';`,
        `    src: url('/fonts/${spec.file}') format('opentype');`,
        `    font-weight: ${spec.weight};`,
        `    font-style: ${spec.style};`,
        '    font-display: swap;',
        '}',
    ].join('\n');
}

function main(): void {
    const fontsDir = path.resolve('public/fonts');
    const outPath = path.resolve('assets/fonts.generated.css');

    const present = SPECS.filter((s) => fs.existsSync(path.join(fontsDir, s.file)));
    const missing = SPECS.filter((s) => !fs.existsSync(path.join(fontsDir, s.file)));

    const banner = [
        '/*',
        ' * AUTO-GENERATED by scripts/build-fonts-css.ts. Do not edit by hand.',
        ' * Emits @font-face rules only for files that exist in public/fonts/.',
        ` * ${present.length}/${SPECS.length} faces resolved at build time.`,
        ' */',
        '',
    ].join('\n');

    const body =
        present.length === 0
            ? '/* No FK Grotesk OTF files found. Falling back to system stack. */\n'
            : present.map(emitFace).join('\n\n') + '\n';

    fs.writeFileSync(outPath, banner + '\n' + body, 'utf-8');

    if (present.length === SPECS.length) {
        console.log(`[fonts] Wrote ${outPath} with all ${SPECS.length} FK Grotesk faces.`);
    } else {
        console.log(
            `[fonts] Wrote ${outPath} with ${present.length}/${SPECS.length} faces. Missing: ${missing.map((m) => m.file).join(', ') || 'none'}`
        );
    }
}

main();
